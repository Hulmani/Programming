
public class HayBlade {
	 int val;
	 int radius;
	public HayBlade()
	{
		
	}
	public HayBlade(int v,int r)
	{
		this.val=v;
		this.radius=r;
	}
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	public int getRadius() {
		return radius;
	}
	public void setRadius(int radius) {
		this.radius = radius;
	}
	
	
}
